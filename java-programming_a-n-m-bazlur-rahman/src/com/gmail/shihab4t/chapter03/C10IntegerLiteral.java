package com.gmail.shihab4t.chapter03;

public class C10IntegerLiteral {
    public static void main(String[] args) {
        int count = 10;

        long count2 = 54L; // this should be used
        long count3 = 54l;

        int decimalValue = 123; // এখানে ডেসিমাল সংখ্যা
        int hexDecimalValue = 0x7B; // এখানে একই সংখ্যা হেক্সডেসিমাল সংখ্যাপদ্ধতিতেে দেখানো হয়েছে।
        int binaryValue = 0b1111011; // এখানে বাইনারিতে দেখানো হয়েছে।
        int octalValue = 01173; // এখানে অক্টালে দেখানো হয়েছে।
    }
}

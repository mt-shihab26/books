package com.gmail.shihab4t.chapter03;

public class C12CharacterAndStringLiteral {
    public static void main(String[] args) {
        char chr = 'A'; // Character Literal
        System.out.println("chr = " + chr);

        String name = "Bazlur"; // String Literal
        System.out.println("name = " + name);

        String greetingInBangla = "সুপ্রভাত";
        System.out.println("greetingInBangla = " + greetingInBangla);

        String greetingsInChines = "你好";
        System.out.println("greetingsInChines = " + greetingsInChines);

        char a = 'a';
        int asciiValue = a;
        System.out.println("asciiValue of a = " + asciiValue);
    }
}

public class C05UnicodesCharactersType {
    public static void main(String[] args) {
        char ch = 'A';
        System.out.println(ch);
        ch = 'b';
        System.out.println(ch);
        ch = '*';
        System.out.println(ch);
        ch = 'à¦–';
        System.out.println(ch);
    }
}
